def romanToInt(s):
    # دیکشنری برای نگاشت نمادهای رومی به اعداد
    Symbol = {
        'M': 1000, 'D': 500, 'C': 100,
        'L': 50, 'X': 10, 'V': 5, 'I': 1
    }
    
    # متغیر برای ذخیره نتیجه نهایی
    result = 0
    
    # حلقه برای پیمایش رشته نمادهای رومی
    for i in range(len(s) - 1):
        # اگر نماد فعلی کوچکتر از نماد بعدی باشد، آن را از نتیجه کم می‌کنیم
        if Symbol[s[i]] < Symbol[s[i + 1]]:
            result -= Symbol[s[i]]
        # در غیر این صورت، آن را به نتیجه اضافه می‌کنیم
        else:
            result += Symbol[s[i]]
    
    # اضافه کردن مقدار آخرین نماد به نتیجه
    result += Symbol[s[-1]]
    
    # برگرداندن نتیجه نهایی
    return result         
    